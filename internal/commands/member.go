// memberFilterFields restricts the listing output to the agent-relevant
// fields by default. Full payload is one --fields away or via 'member get'.
const memberFilterDefaultFields = "id,username,email,isApproved,isLockedOut,isTwoFactorEnabled,failedPasswordAttempts,groups"

func memberList(deps Dependencies) *cobra.Command {
	var fields string
	var filter string
	var skip int
	var take int
	var triage readTriageOptions
	cmd := &cobra.Command{
		Use:   "list",
		Short: "List members (paginated; use --filter for substring search)",
		RunE: func(cmd *cobra.Command, args []string) error {
			params := map[string]any{}
			if strings.TrimSpace(filter) != "" {
				params["filter"] = filter
			}
			if cmd.Flags().Changed("skip") {
				params["skip"] = skip
			}
			if cmd.Flags().Changed("take") {
				params["take"] = take
			}
			result, err := deps.Client.Get(context.Background(), "/filter/member", api.RequestOptions{Fields: fields, Params: params})
			if err != nil {
				return err
			}
			return printResult(cmd, deps, applyReadTriage(applyFieldsProjection(result, fields), triage))
		},
	}
	cmd.Flags().StringVar(&fields, "fields", "", "Limit response fields (defaults to all; pass id,username,email,... for agent-friendly summaries)")
	cmd.Flags().StringVar(&filter, "filter", "", "Substring filter against member username/email")
	cmd.Flags().IntVar(&skip, "skip", 0, "Skip count")
	cmd.Flags().IntVar(&take, "take", 0, "Take count (0 = server default)")
	addReadTriageFlags(cmd, &triage)
	return cmd
}

