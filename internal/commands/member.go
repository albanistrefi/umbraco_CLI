func memberCreate(deps Dependencies) *cobra.Command {
	var jsonPayload string
	var dryRun bool
	var printTemplate bool
	cmd := &cobra.Command{
		Use:   "create",
		Short: "Create a member",
		Long:  "POST /member. Use --print-template to see the accepted payload shape. The 'id' field is generated by the CLI if omitted.",
		RunE: func(cmd *cobra.Command, args []string) error {
			if printTemplate {
				return printResult(cmd, deps, schema.Templates["member.create"])
			}
			if err := requireValue("--json", jsonPayload); err != nil {
				return err
			}
			body, err := parsePayload(jsonPayload)
			if err != nil {
				return err
			}
			if _, err := ensurePayloadID(body); err != nil {
				return err
			}
			result, err := deps.Client.Post(context.Background(), memberPath, body, api.RequestOptions{DryRun: dryRun})
			if err != nil {
				return err
			}
			return printResult(cmd, deps, createResult(result, body, "icon"))
		},
	}
	cmd.Flags().StringVar(&jsonPayload, "json", "", "Create payload as JSON")
	cmd.Flags().BoolVar(&dryRun, "dry-run", false, "Validate request without executing")
	cmd.Flags().BoolVar(&printTemplate, "print-template", false, "Print an annotated JSON skeleton; substitute placeholders before passing to --json")
	return cmd
}

func memberUpdate(deps Dependencies) *cobra.Command {
	var jsonPayload string
	var mergeJSON string
	var dryRun bool
	cmd := &cobra.Command{
		Use:   "update <id>",
		Short: "Update a member (fetch-and-merge)",
		Long: `Both --json and --merge-json fetch the current member, deep-merge the patch, and PUT the merged result. This matches the datatype/document fix: an unmentioned field is never silently dropped.

Known API limitation (Umbraco 17.x): the Management API's UpdateMemberRequestModel does NOT accept the following fields, even though they appear on the read model. Including them in the patch returns 204 but the server-side value does not change:
  - isApproved
  - isLockedOut
  - failedPasswordAttempts

These are managed by the auth subsystem (login flow / backoffice action). The CLI does not silently rewrite the request — it sends what you ask — but agents should be aware that mutating these via 'member update' is a no-op.`,
		Args:  cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) error {
			hasJSON := strings.TrimSpace(jsonPayload) != ""
			hasMergeJSON := strings.TrimSpace(mergeJSON) != ""
			if hasJSON == hasMergeJSON {
				return fmt.Errorf("member update requires exactly one of --json or --merge-json")
			}
			raw := jsonPayload
			if hasMergeJSON {
				raw = mergeJSON
			}
			patch, err := parsePayload(raw)
			if err != nil {
				return err
			}
			current, err := fetchMemberObject(context.Background(), deps.Client, args[0])
			if err != nil {
				return err
			}
			merged := mergeAliasPayload(current, patch)
			result, err := deps.Client.Put(context.Background(), fmt.Sprintf("%s/%s", memberPath, args[0]), merged, api.RequestOptions{DryRun: dryRun, SkipValidation: true})
			if err != nil {
				return err
			}
			if !dryRun && result == nil {
				return printResult(cmd, deps, map[string]any{"updated": true})
			}
			return printResult(cmd, deps, result)
		},
	}
	cmd.Flags().StringVar(&jsonPayload, "json", "", "Update payload as JSON; merged into the current member so fields not mentioned are preserved")
	cmd.Flags().StringVar(&mergeJSON, "merge-json", "", "Alias for --json")
	cmd.Flags().BoolVar(&dryRun, "dry-run", false, "Validate request without executing")
	return cmd
}

